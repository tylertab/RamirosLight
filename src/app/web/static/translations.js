export function applyLocale(locale, { switcher } = {}) {
  const resolved = translations[locale] ? locale : "en";
  const dictionary = translations[resolved];
  document.documentElement.lang = resolved;
  document.querySelectorAll("[data-l10n-key]").forEach((element) => {
    const key = element.dataset.l10nKey;
    if (key in dictionary) {
      element.textContent = dictionary[key];
    }
  });
  document.querySelectorAll("[data-l10n-placeholder]").forEach((element) => {
    const key = element.dataset.l10nPlaceholder;
    if (key in dictionary) {
      element.setAttribute("placeholder", dictionary[key]);
    }
  });
  document.querySelectorAll("[data-l10n-aria-label]").forEach((element) => {
    const key = element.dataset.l10nAriaLabel;
    if (key in dictionary) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });
  if (switcher) {
    switcher.value = resolved;
  }
  try {
    localStorage.setItem("trackeo.locale", resolved);
  } catch (error) {
    // ignore storage issues
  }
  window.dispatchEvent(
    new CustomEvent("trackeo:locale-change", { detail: { locale: resolved } })
  );
  return resolved;
}

export function readStoredLocale() {
  try {
    return localStorage.getItem("trackeo.locale");
  } catch (error) {
    return null;
  }
}
